#!/usr/bin/env python3
"""Generate RESULTS.md from the raw wrk reports in results/raw/.

Each raw file is named  {stack}__{endpoint}__t{T}c{C}d{D}__run{R}.txt  and holds
one wrk --latency report. We group by (stack, endpoint, stage), report the MEDIAN
requests/sec across repeats, and — for the run that produced that median — its
average latency and error counts. Min/max req/s across repeats are shown too so
the spread is visible.
"""
from __future__ import annotations
import glob
import os
import re
import statistics
from collections import defaultdict

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
RAW = os.path.join(ROOT, "results", "raw")

# Presentation order.
STACK_ORDER = ["doppar-fpm", "laravel", "symfony", "doppar-worker"]
STACK_LABEL = {
    "doppar-fpm": "Doppar (PHP-FPM)",
    "laravel": "Laravel (PHP-FPM)",
    "symfony": "Symfony (PHP-FPM)",
    "doppar-worker": "Doppar (FrankenPHP worker) ¹",
}
ENDPOINT_ORDER = ["json", "db"]
ENDPOINT_LABEL = {"json": "/json (static JSON)", "db": "/db (ORM primary-key lookup)"}
STAGE_ORDER = [(2, 50, 20), (4, 200, 30), (8, 500, 60)]

FNAME = re.compile(r"^(?P<stack>.+?)__(?P<ep>json|db)__t(?P<t>\d+)c(?P<c>\d+)d(?P<d>\d+)__run(?P<r>\d+)\.txt$")


def parse_report(path: str):
    txt = open(path, encoding="utf-8", errors="replace").read()
    out = {"rps": None, "lat_ms": None, "non2xx": 0, "sock": 0}
    m = re.search(r"^Requests/sec:\s*([\d.]+)", txt, re.M)
    if m:
        out["rps"] = float(m.group(1))
    m = re.search(r"^\s*Latency\s+([\d.]+)(us|ms|s)\b", txt, re.M)
    if m:
        v, u = float(m.group(1)), m.group(2)
        out["lat_ms"] = v / 1000 if u == "us" else (v * 1000 if u == "s" else v)
    m = re.search(r"Non-2xx or 3xx responses:\s*(\d+)", txt)
    if m:
        out["non2xx"] = int(m.group(1))
    m = re.search(r"Socket errors:.*?connect (\d+), read (\d+), write (\d+), timeout (\d+)", txt)
    if m:
        out["sock"] = sum(int(x) for x in m.groups())
    return out


def fmt_lat(ms):
    if ms is None:
        return "n/a"
    return f"{ms:.1f} ms" if ms >= 1 else f"{ms*1000:.0f} µs"


def fmt_err(non2xx, sock):
    if non2xx == 0 and sock == 0:
        return "0"
    parts = []
    if non2xx:
        parts.append(f"{non2xx} non-2xx")
    if sock:
        parts.append(f"{sock} socket")
    return ", ".join(parts)


def main() -> int:
    files = glob.glob(os.path.join(RAW, "*.txt"))
    if not files:
        print("No raw reports found in results/raw/. Run bench/run.sh first.")
        return 1

    # grouped[(stack, ep, stage)] = list of parsed run dicts
    grouped: dict = defaultdict(list)
    for path in files:
        m = FNAME.match(os.path.basename(path))
        if not m:
            continue
        stage = (int(m["t"]), int(m["c"]), int(m["d"]))
        rec = parse_report(path)
        if rec["rps"] is None:
            continue
        grouped[(m["stack"], m["ep"], stage)].append(rec)

    stacks_present = [s for s in STACK_ORDER if any(k[0] == s for k in grouped)]
    stacks_present += sorted({k[0] for k in grouped} - set(STACK_ORDER))

    def median_record(recs):
        recs = sorted(recs, key=lambda x: x["rps"])
        med_rps = statistics.median(r["rps"] for r in recs)
        rep = recs[len(recs) // 2]  # representative run (middle by rps)
        return med_rps, rep, recs[0]["rps"], recs[-1]["rps"]

    env = ""
    env_path = os.path.join(ROOT, "results", "env.txt")
    if os.path.exists(env_path):
        env = open(env_path, encoding="utf-8").read().strip()

    L = []
    L.append("# Benchmark Results")
    L.append("")
    L.append("Median requests/sec across repeats, per stack × endpoint × load stage. "
             "Generated by `bench/gen_results.py` from the raw wrk reports in `results/raw/`.")
    L.append("")
    if env:
        L.append("```")
        L.append(env)
        L.append("```")
        L.append("")

    # ---- headline: median req/s, stages as columns ----
    for ep in ENDPOINT_ORDER:
        if not any(k[1] == ep for k in grouped):
            continue
        L.append(f"## {ENDPOINT_LABEL.get(ep, ep)}")
        L.append("")
        header = "| Stack | " + " | ".join(f"{t}t / {c}c / {d}s" for (t, c, d) in STAGE_ORDER) + " |"
        sep = "|" + "---|" * (len(STAGE_ORDER) + 1)
        L.append(header)
        L.append(sep)
        for stack in stacks_present:
            cells = []
            for stage in STAGE_ORDER:
                recs = grouped.get((stack, ep, stage))
                if not recs:
                    cells.append("–")
                    continue
                med, rep, lo, hi = median_record(recs)
                cells.append(f"**{med:,.0f}** req/s")
            L.append(f"| {STACK_LABEL.get(stack, stack)} | " + " | ".join(cells) + " |")
        L.append("")

    # ---- detail: every metric ----
    L.append("## Detailed metrics")
    L.append("")
    L.append("`req/s` is the median of the repeats; latency and errors are from the median run; "
             "`spread` is min–max req/s across repeats.")
    L.append("")
    L.append("| Stack | Endpoint | Stage (t/c/d) | Median req/s | Avg latency | Errors | Spread (req/s) |")
    L.append("|---|---|---|---|---|---|---|")
    for stack in stacks_present:
        for ep in ENDPOINT_ORDER:
            for stage in STAGE_ORDER:
                recs = grouped.get((stack, ep, stage))
                if not recs:
                    continue
                med, rep, lo, hi = median_record(recs)
                t, c, d = stage
                L.append(
                    f"| {STACK_LABEL.get(stack, stack)} | /{ep} | {t}/{c}/{d}s | "
                    f"{med:,.0f} | {fmt_lat(rep['lat_ms'])} | {fmt_err(rep['non2xx'], rep['sock'])} | "
                    f"{lo:,.0f}–{hi:,.0f} |"
                )
    L.append("")
    L.append("¹ **Doppar (FrankenPHP worker)** is experimental and NOT a controlled comparison: "
             "it swaps nginx+php-fpm for FrankenPHP/Caddy, so it changes the web server AND the runtime "
             "model at once. Read it as “can Doppar run as a persistent worker, and roughly how fast”, "
             "not as a like-for-like row against the PHP-FPM stacks. See README.")
    L.append("")

    out_path = os.path.join(ROOT, "RESULTS.md")
    open(out_path, "w", encoding="utf-8").write("\n".join(L))
    print(f"Wrote {out_path} ({len(files)} raw reports parsed).")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
